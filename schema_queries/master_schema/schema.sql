CREATE TABLE sales_region_data (
    account_no VARCHAR(50) NOT NULL PRIMARY KEY,  
    records VARCHAR(50) NOT NULL,
    territory VARCHAR(20) NOT NULL CHECK (territory IN ('MCOW', 'Western', 'Central', 'Midwest', 'Northeast', 'Southwest', 'Pacific', 'Mountain', 'Northeast', 'Atlantic', 'Southeast', 'East', 'West', 'Special')),
    latitude DECIMAL(9, 6) DEFAULT NULL CHECK (latitude >= -90 AND latitude <= 90 OR latitude IS NULL),
    year INT NOT NULL CHECK (year BETWEEN 2018 AND 2024),
    month_1 NUMERIC(10, 2) DEFAULT 0.0,
    month_2 NUMERIC(10, 2) DEFAULT 0.0,
    month_3 NUMERIC(10, 2) DEFAULT 0.0,
    month_4 NUMERIC(10, 2) DEFAULT 0.0,
    month_5 NUMERIC(10, 2) DEFAULT 0.0,
    month_6 NUMERIC(10, 2) DEFAULT 0.0,
    month_7 NUMERIC(10, 2) DEFAULT 0.0,
    month_8 NUMERIC(10, 2) DEFAULT 0.0,
    month_9 NUMERIC(10, 2) DEFAULT 0.0,
    month_10 NUMERIC(10, 2) DEFAULT 0.0,
    month_11 NUMERIC(10, 2) DEFAULT 0.0,
    month_12 NUMERIC(10, 2) DEFAULT 0.0,
    q1 NUMERIC(10, 2) DEFAULT 0.0,
    q2 NUMERIC(10, 2) DEFAULT 0.0,
    q3 NUMERIC(10, 2) DEFAULT 0.0,
    q4 NUMERIC(10, 2) DEFAULT 0.0,
    sales NUMERIC(12, 2) DEFAULT 0.0 CHECK (sales >= 0),
    individual INT DEFAULT 0 CHECK (individual >= 0),
    q1_calculated NUMERIC(12, 2) DEFAULT 0.0,
    q2_calculated NUMERIC(12, 2) DEFAULT 0.0,
    q3_calculated NUMERIC(12, 2) DEFAULT 0.0,
    q4_calculated NUMERIC(12, 2) DEFAULT 0.0,
    individual_calculated NUMERIC(12, 2) DEFAULT 0.0,
    territory_group VARCHAR(15) CHECK (territory_group IN ('Special', 'West', 'Midwest', 'East', 'South'))
);
